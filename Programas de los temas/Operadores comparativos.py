numero1 = float(input("Introduce el primer número: "))
numero2 = float(input("Introduce el segundo número: "))

print("Operaciones comparativas:")
                      
print(f"¿Es numero1 igual a numero2? {numero1 == numero2}")
print(f"¿Es numero1 diferente de numero2? {numero1 != numero2}")
print(f"¿Es numero1 mayor que numero2? {numero1 > numero2}")
print(f"¿Es numero1 menor que numero2? {numero1 < numero2}")
print(f"¿Es numero1 mayor o igual que numero2? {numero1 >= numero2}")
print(f"¿Es numero1 menor o igual que numero2? {numero1 <= numero2}")
